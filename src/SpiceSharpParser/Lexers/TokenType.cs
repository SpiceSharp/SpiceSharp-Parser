namespace SpiceSharpParser.Lexers
{
    /// <summary>
    /// Base token types.
    /// </summary>
    public enum TokenType
    {
        /// <summary>
        /// Represents 'the end of file' token type
        /// </summary>
        EOF = -1,

        /// <summary>
        /// Represents unknown token type
        /// </summary>
        Unknown = 0,
    }
}
